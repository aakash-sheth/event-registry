export const DEFAULT_COUNTRY = 'IN'
export const DEFAULT_COUNTRY_CODE = '+91'

export function getCountryCode(countryIso: string): string {
  return COUNTRY_CODES[countryIso]?.code || DEFAULT_COUNTRY_CODE
}

export function formatPhoneWithCountryCode(phone: string, countryCode: string): string {
  // Remove any whitespace, +, dashes, parentheses
  let cleaned = phone.trim().replace(/\s+/g, '').replace(/-/g, '').replace(/\(/g, '').replace(/\)/g, '')
  
  // Remove any non-digit characters to get just the phone digits
  const phoneDigits = cleaned.replace(/\D/g, '')
  
  if (!phoneDigits) return phone
  
  // If country code is provided, use it directly - don't try to detect from phone number
  const codeDigits = countryCode.replace('+', '')
  
  // Remove leading 0 if present (common in India)
  let localNumber = phoneDigits
  if (localNumber.startsWith('0') && countryCode === '+91') {
    localNumber = localNumber.slice(1)
  }
  
  // If phone already starts with country code digits, remove them to get local number
  if (localNumber.startsWith(codeDigits)) {
    localNumber = localNumber.slice(codeDigits.length)
  }
  
  // Return formatted phone with country code
  return `+${codeDigits}${localNumber}`
}

